def selected_arxiv_papers(payload: dict[str, Any], selection_payload: Any | None = None) -> tuple[list[dict[str, Any]], list[dict[str, Any]]]:
    selected = resolve_selected_papers(payload, selection_payload)
    papers: list[dict[str, Any]] = []
    skipped: list[dict[str, Any]] = []
    seen: set[str] = set()
    for item in selected:
        paper = item["paper"]
        arxiv_id = arxiv_id_from_paper(paper)
        if not arxiv_id:
            skipped.append({"title": paper_title(paper), "reason": "missing arXiv id"})
            continue
        if arxiv_id in seen:
            continue
        seen.add(arxiv_id)
        papers.append(
            {
                "arxiv_id": arxiv_id,
                "title": paper_title(paper),
                "paper": paper,
                "topics": item.get("topics", []),
                "sources": paper.get("__sources", []),
                "indexes": paper.get("__indexes", []),
            }
        )
    return papers, skipped


def process_selected_paper(
    *,
    item: dict[str, Any],
    base_url: str,
    timeout: float,
    library_id: int,
    download_pdfs: bool,
    pdf_timeout: float,
    strict_pdf: bool,
) -> dict[str, Any]:
    client = HttpClient(base_url, timeout=timeout)
    arxiv_id = item["arxiv_id"]
    paper = item["paper"]
    runtime_plan_entry: dict[str, Any] | None = None

    match = find_existing_item(client, arxiv_id, item["title"])
    if match:
        item_key = first_text(match.get("key"))
        if not item_key:
            raise ZoteroSyncError(f"Matched arXiv:{arxiv_id}, but Zotero returned an item without a key")
        bibtex_key = export_item_bibtex_key(client, item_key)
        action = "already_in_zotero"
        pdf_status = item_pdf_status(client, item_key, arxiv_id)
        pdf_result = {
            "attempted": False,
            "checked": True,
            "ok": bool(pdf_status.get("ok")),
            "already_present": bool(pdf_status.get("ok")),
            "status": pdf_status,
        }
        if download_pdfs and not pdf_status.get("ok"):
            runtime_plan_entry = runtime_pdf_plan_entry(
                library_id=library_id,
                item_key=item_key,
                arxiv_id=arxiv_id,
                title=item["title"],
                paper=paper,
            )
            pdf_result.update(
                {
                    "requires_runtime_attachment": True,
                    "url": runtime_plan_entry["pdfUrl"],
                }
            )
        elif strict_pdf and not pdf_status.get("ok"):
            raise ZoteroSyncError(f"Existing Zotero item for arXiv:{arxiv_id} has no PDF attachment")
    else:
        imported_result = import_paper(
            client,
            paper,
            arxiv_id,
            download_pdf=download_pdfs,
            pdf_timeout=pdf_timeout,
            strict_pdf=strict_pdf,
        )
        item_key = imported_result["item_key"]
        bibtex_key = imported_result["bibtex_key"]
        action = "imported"
        pdf_result = imported_result["pdf"]
        if download_pdfs and not pdf_result.get("verified"):
            runtime_plan_entry = runtime_pdf_plan_entry(
                library_id=library_id,
                item_key=item_key,
                arxiv_id=arxiv_id,
                title=item["title"],
                paper=paper,
            )
            pdf_result["requires_runtime_attachment"] = True
            pdf_result.setdefault("url", runtime_plan_entry["pdfUrl"])

    action_record = {
        "action": action,
        "arxiv_id": arxiv_id,
        "title": item["title"],
        "zotero_item_key": item_key,
        "bibtex_key": bibtex_key,
        "topics": item["topics"],
        "pdf": pdf_result,
    }
    return {
        "arxiv_id": arxiv_id,
        "action_record": action_record,
        "runtime_pdf_plan_entry": runtime_plan_entry,
    }


def sync_selected_to_zotero(args: argparse.Namespace) -> dict[str, Any]:
    payload = load_json(args.input)
    if not isinstance(payload, dict):
        raise TypeError("Input JSON must be an object.")
    selection_payload = load_json(args.selection) if args.selection else None
    date = first_text(args.date, payload.get("date"), payload.get("selectedDate"))
    if not date:
        date = dt.date.today().isoformat()

    papers, skipped = selected_arxiv_papers(payload, selection_payload)
    if args.limit is not None:
        papers = papers[: args.limit]

    client = HttpClient(args.base_url, timeout=args.timeout)
    target_path = f"/{args.library_id}/{args.root_collection}/{date}"

    report: dict[str, Any] = {
        "date": date,
        "target_collection_path": target_path,
        "input": str(Path(args.input).resolve()) if args.input != "-" else "-",
        "selected_arxiv_papers": len(papers),
        "skipped": skipped,
        "dry_run": not args.yes and not args.verify_pdfs_only,
        "verify_pdfs_only": args.verify_pdfs_only,
        "download_pdf": args.download_pdfs,
        "actions": [],
    }

    if not papers:
        report["message"] = "No selected papers with arXiv IDs."
        return report

    if args.yes or args.check_zotero or args.verify_pdfs_only:
        report["zotero"] = ensure_zotero_ready(client)

    if args.verify_pdfs_only:
        missing_pdf: list[dict[str, Any]] = []
        for item in papers:
            arxiv_id = item["arxiv_id"]
            match = find_existing_item(client, arxiv_id, item["title"])
            if not match:
                missing = {
                    "arxiv_id": arxiv_id,
                    "title": item["title"],
                    "reason": "missing Zotero item",
                }
                missing_pdf.append(missing)
                report["actions"].append({"action": "verify_pdf", **missing, "pdf": {"checked": False, "ok": False}})
                continue
            item_key = first_text(match.get("key"))
            if not item_key:
                missing = {
                    "arxiv_id": arxiv_id,
                    "title": item["title"],
                    "reason": "Zotero item has no key",
                }
                missing_pdf.append(missing)
                report["actions"].append({"action": "verify_pdf", **missing, "pdf": {"checked": False, "ok": False}})
                continue
            pdf_status = item_pdf_status(client, item_key, arxiv_id)
            action = {
                "action": "verify_pdf",
                "arxiv_id": arxiv_id,
                "title": item["title"],
                "zotero_item_key": item_key,
                "pdf": pdf_status,
            }
            report["actions"].append(action)
            if not pdf_status.get("ok"):
                missing_pdf.append(
                    {
                        "arxiv_id": arxiv_id,
                        "title": item["title"],
                        "zotero_item_key": item_key,
                        "reason": "missing PDF attachment",
                    }
                )
        report["pdf_missing_after_verify"] = missing_pdf
        if missing_pdf and args.strict_pdf:
            raise ZoteroSyncError(f"PDF verification failed for {len(missing_pdf)} selected paper(s)", report)
        return report

    if not args.yes:
        report["actions"] = [
            {
                "action": "would_sync",
                "arxiv_id": item["arxiv_id"],
                "title": item["title"],
                "topics": item["topics"],
                "indexes": item["indexes"],
            }
            for item in papers
        ]
        return report

    collections = list_collections(client)
    root_key = find_collection_key(collections, args.root_collection, False)
    if not root_key:
        raise ZoteroSyncError(f"Root collection not found: {args.root_collection}")
    date_key = find_collection_key(collections, date, root_key)
    existing_citekeys = collection_bibtex_keys(client, date_key) if date_key else []

    citekeys = list(existing_citekeys)
    citekey_set = set(citekeys)
    checkpoint_path = checkpoint_path_for_args(args, date)
    completed_checkpoint = load_checkpoint_completed(checkpoint_path) if args.resume else {}
    resumed_from_checkpoint = 0
    failures: list[dict[str, Any]] = []
    action_slots: dict[int, dict[str, Any]] = {}
    runtime_pdf_plan_by_key: dict[str, dict[str, Any]] = {}

    report["checkpoint"] = {
        "enabled": checkpoint_path is not None,
        "path": str(checkpoint_path) if checkpoint_path else None,
        "resume": bool(args.resume),
        "completed_loaded": len(completed_checkpoint),
    }
    report["workers"] = max(1, int(args.workers))

    def remember_result(order: int, result: dict[str, Any], *, from_checkpoint: bool = False) -> None:
        nonlocal resumed_from_checkpoint
        action_record = dict(result["action_record"])
        if from_checkpoint:
            action_record["resumed_from_checkpoint"] = True
            resumed_from_checkpoint += 1
        action_slots[order] = action_record

        bibtex_key = first_text(action_record.get("bibtex_key"))
        if bibtex_key and bibtex_key not in citekey_set:
            citekeys.append(bibtex_key)
            citekey_set.add(bibtex_key)

        plan_entry = result.get("runtime_pdf_plan_entry")
        if isinstance(plan_entry, dict):
            plan_key = f"{plan_entry.get('libraryID')}:{plan_entry.get('itemKey')}:{plan_entry.get('pdfUrl')}"
            runtime_pdf_plan_by_key[plan_key] = plan_entry

    pending_jobs: list[tuple[int, dict[str, Any]]] = []
    for order, item in enumerate(papers):
        checkpoint_event = completed_checkpoint.get(item["arxiv_id"])
        if checkpoint_event:
            checkpoint_result = {
                "action_record": checkpoint_event["result"],
                "runtime_pdf_plan_entry": checkpoint_event.get("runtime_pdf_plan_entry"),
            }
            remember_result(order, checkpoint_result, from_checkpoint=True)
            progress_log(args.progress, f"[resume] {item['arxiv_id']} {item['title']}")
        else:
            pending_jobs.append((order, item))

    max_workers = max(1, int(args.workers))
    if pending_jobs:
        progress_log(args.progress, f"[sync] processing {len(pending_jobs)} paper(s) with workers={max_workers}")

    def record_failure(order: int, item: dict[str, Any], exc: Exception) -> None:
        failure = {
            "arxiv_id": item["arxiv_id"],
            "title": item["title"],
            "order": order,
            "error": str(exc),
        }
        failures.append(failure)
        append_checkpoint_event(
            checkpoint_path,
            {
                "event": "paper_failed",
                "date": date,
                "arxiv_id": item["arxiv_id"],
                "title": item["title"],
                "order": order,
                "timestamp": utc_now_iso(),
                "error": str(exc),
            },
        )
        progress_log(args.progress, f"[fail] {item['arxiv_id']} {exc}")

    def record_completed(order: int, item: dict[str, Any], result: dict[str, Any]) -> None:
        remember_result(order, result)
        append_checkpoint_event(
            checkpoint_path,
            {
                "event": "paper_completed",
                "date": date,
                "arxiv_id": item["arxiv_id"],
                "title": item["title"],
                "order": order,
                "timestamp": utc_now_iso(),
                "result": result["action_record"],
                "runtime_pdf_plan_entry": result.get("runtime_pdf_plan_entry"),
            },
        )
        progress_log(args.progress, f"[done] {item['arxiv_id']} {result['action_record']['action']}")

    def checkpoint_started(order: int, item: dict[str, Any]) -> None:
        append_checkpoint_event(
            checkpoint_path,
            {
                "event": "paper_started",
                "date": date,
                "arxiv_id": item["arxiv_id"],
                "title": item["title"],
                "order": order,
                "timestamp": utc_now_iso(),
            },
        )
        progress_log(args.progress, f"[start] {item['arxiv_id']} {item['title']}")

    if max_workers == 1:
        for order, item in pending_jobs:
            checkpoint_started(order, item)
            try:
                result = process_selected_paper(
                    item=item,
                    base_url=args.base_url,
                    timeout=args.timeout,
                    library_id=args.library_id,
                    download_pdfs=args.download_pdfs,
                    pdf_timeout=args.pdf_timeout,
                    strict_pdf=args.strict_pdf,
                )
                record_completed(order, item, result)
            except Exception as exc:
                record_failure(order, item, exc)
    elif pending_jobs:
        with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as executor:
            future_to_job: dict[concurrent.futures.Future[dict[str, Any]], tuple[int, dict[str, Any]]] = {}
            for order, item in pending_jobs:
                checkpoint_started(order, item)
                future = executor.submit(
                    process_selected_paper,
                    item=item,
                    base_url=args.base_url,
                    timeout=args.timeout,
                    library_id=args.library_id,
                    download_pdfs=args.download_pdfs,
                    pdf_timeout=args.pdf_timeout,
                    strict_pdf=args.strict_pdf,
                )
                future_to_job[future] = (order, item)

            for future in concurrent.futures.as_completed(future_to_job):
                order, item = future_to_job[future]
                try:
                    record_completed(order, item, future.result())
                except Exception as exc:
                    record_failure(order, item, exc)

    report["actions"] = [action_slots[index] for index in sorted(action_slots)]
    imported = sum(1 for action in report["actions"] if action.get("action") == "imported")
    existing = sum(1 for action in report["actions"] if action.get("action") == "already_in_zotero")
    runtime_pdf_plan = list(runtime_pdf_plan_by_key.values())

    runtime_pdf_artifacts: dict[str, Any] | None = None
    if runtime_pdf_plan and args.runtime_pdf_helper:
        runtime_pdf_artifacts = write_runtime_pdf_artifacts(
            plan=runtime_pdf_plan,
            plan_path=Path(args.runtime_pdf_plan_out or f"work/zotero_attach_missing_pdfs_{date}.json").resolve(),
            script_path=Path(args.runtime_pdf_script_out or f"work/zotero_attach_missing_pdfs_{date}.js").resolve(),
        )
    elif runtime_pdf_plan:
        runtime_pdf_artifacts = {"count": len(runtime_pdf_plan), "disabled": True}

    aux_path = Path(args.aux_out or f"work/zotero_daily_papers_{date}.aux").resolve()
    write_aux(aux_path, citekeys)
    bbt_result = client.bbt_rpc("collection.scanAUX", [target_path, str(aux_path)])

    collections_after = list_collections(client)
    date_key_after = find_collection_key(collections_after, date, root_key) or (
        bbt_result.get("key") if isinstance(bbt_result, dict) else None
    )
    final_citekeys = collection_bibtex_keys(client, date_key_after) if date_key_after else []
    missing_after = sorted(citekey_set - set(final_citekeys))

    report.update(
        {
            "root_collection_key": root_key,
            "date_collection_key": date_key_after,
            "existing_before": len(existing_citekeys),
            "already_in_zotero": existing,
            "imported": imported,
            "resumed_from_checkpoint": resumed_from_checkpoint,
            "processed_successfully": len(report["actions"]),
            "failed": len(failures),
            "failures": failures,
            "runtime_pdf_attachment_plan": runtime_pdf_artifacts
            or {"count": 0, "plan_path": None, "script_path": None},
            "aux_path": str(aux_path),
            "better_bibtex_result": bbt_result,
            "final_collection_bibtex_keys": len(final_citekeys),
            "missing_after_sync": missing_after,
        }
    )
    if failures:
        raise ZoteroSyncError(f"Zotero processing failed for {len(failures)} selected paper(s)", report)
    if missing_after:
        raise ZoteroSyncError(f"Collection sync verification failed; missing citekeys: {missing_after}", report)
    return report


