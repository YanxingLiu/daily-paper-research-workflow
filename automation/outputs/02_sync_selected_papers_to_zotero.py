#!/usr/bin/env python3
"""Sync selected daily arXiv papers into a Zotero daily collection.

This script is intentionally deterministic and consumes the same JSON input as
``04_daily_paper_brief.py``. The AI only fills the ``selected`` field; this script
does the Zotero side:

1. Resolve unique selected papers with arXiv IDs.
2. Find or import missing papers into Zotero through the Zotero Connector.
3. Attach PDFs for newly imported papers through the Connector, and generate a
   deterministic Zotero-runtime helper when an existing item is missing its PDF.
4. Rebuild ``Arxiv-Daily-Papers/<date>`` with the existing citation keys plus
   the newly selected citation keys through Better BibTeX ``collection.scanAUX``.

It never writes Zotero's SQLite database directly.
"""

