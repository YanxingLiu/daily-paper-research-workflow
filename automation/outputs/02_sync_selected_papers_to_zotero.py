def append_checkpoint_event(path: Path | None, event: dict[str, Any]) -> None:
    if path is None:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("a", encoding="utf-8") as handle:
        handle.write(json.dumps(event, ensure_ascii=False, sort_keys=True) + "\n")
        handle.flush()


def load_checkpoint_completed(path: Path | None) -> dict[str, dict[str, Any]]:
    if path is None or not path.exists():
        return {}
    completed: dict[str, dict[str, Any]] = {}
    with path.open("r", encoding="utf-8") as handle:
        for line in handle:
            line = line.strip()
            if not line:
                continue
            try:
                event = json.loads(line)
            except json.JSONDecodeError:
                continue
            if event.get("event") != "paper_completed":
                continue
            arxiv_id = first_text(event.get("arxiv_id"))
            result = event.get("result")
            if arxiv_id and isinstance(result, dict):
                completed[arxiv_id] = event
    return completed


def action_needs_runtime_pdf(action: dict[str, Any]) -> bool:
    pdf = action.get("pdf")
    return isinstance(pdf, dict) and bool(pdf.get("requires_runtime_attachment"))


