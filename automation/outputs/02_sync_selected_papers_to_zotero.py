    parser.add_argument("--library-id", type=int, default=1, help="Better BibTeX library ID. Default: 1.")
    parser.add_argument("--base-url", default=DEFAULT_BASE_URL)
    parser.add_argument("--timeout", type=float, default=30.0)
    parser.add_argument("--pdf-timeout", type=float, default=90.0)
    parser.add_argument("--aux-out", help="Path for generated AUX file.")
    parser.add_argument("--runtime-pdf-plan-out", help="Path for generated missing-PDF attachment plan JSON.")
